package lattice;
public class Tuple {
	public Integer x;
	public Float y;
	public Tuple(Integer x, Float y) {
		this.x = x;
		this.y = y;
	}
	public Integer getX() {
		return x;
	}
	public void setX(Integer x) {
		this.x = x;
	}
	public Float getY() {
		return y;
	}
	public void setY(Float y) {
		this.y = y;
	}
}
